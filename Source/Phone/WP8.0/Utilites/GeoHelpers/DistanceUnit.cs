namespace SOS.Phone.Pages
{
    /// <summary>
    /// This enum represents distance units that are
    /// currently used by the Geo namespace classes.
    /// </summary>
    
    public enum DistanceUnit
    {
        /// <summary>
        /// 1 kilometer = 0,62137119 miles
        /// </summary>
        Kilometers = 1,

        /// <summary>
        /// 1 mile = 1,609344 kilometers.
        /// </summary>
        Miles = 0
    }
}